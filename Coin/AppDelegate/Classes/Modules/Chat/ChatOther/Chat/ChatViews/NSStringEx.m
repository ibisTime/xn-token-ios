-(NSString *) pinyinString
{
    if(self == nil)
    {
        return nil;
    }
    
    NSMutableString *py = nil;
    unsigned char * uniStr = NULL;
    
    @try
    {
        NSUInteger uLen = [self length] * 2;
        
        uniStr = (unsigned char *)malloc(uLen * 2 + 1);
        if(uniStr == NULL)
        {
            NSException *e = [NSException exceptionWithName:@"Not Enough Memory!" reason:@"memory full" userInfo:nil];
            @throw e;
        }
        
        NSRange r;
        r.location = 0;
        r.length = [self length];// lengthOfBytesUsingEncoding:NSUTF8StringEncoding];
        [self getBytes:uniStr maxLength:uLen * 2 usedLength:0 encoding:NSUTF16BigEndianStringEncoding options: NSStringEncodingConversionAllowLossy range:r remainingRange:NULL];
        
        char* lzpy = malloc(uLen + 1);
        if (lzpy == 0){
            return nil;
        }
        memset(lzpy, 0, uLen + 1);
        int pos = 0;
        for(int i = 0; i < uLen; i = i + 2)
        {
            unsigned char c1 = *(uniStr + i);
            unsigned char c2 = *(uniStr + i + 1);
            if(c1 * 256 + c2 >= 19968 && c1 * 256 + c2 <= 40869)
            {
                lzpy[pos] = ChineseFirstPyTable[c1 * 256 + c2 - 19968];
                pos++;
            }
            else if(c1 == 0 && c2 >= 65 && c2 <= 90)
            {
                lzpy[pos] = c2;
                pos++;
            }
            else if(c1 == 0 && c2 >= 97 && c2 <= 122)
            {
                lzpy[pos] = c2 - 32;
                pos++;
            }
        }
        py = [NSMutableString stringWithUTF8String:lzpy];
        free(lzpy);
        lzpy = 0;
    }
    @catch(...)
    {
        DebugLog(@"Convert %@ to Pinyin failed!", self);
        py = nil;
    }
    @finally
    {
        if(uniStr != NULL)
        {
            free(uniStr);
            uniStr = NULL;
        }
    }
    
    return py;
