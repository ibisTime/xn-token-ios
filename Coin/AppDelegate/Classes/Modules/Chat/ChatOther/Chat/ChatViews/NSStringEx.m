        lzpy = 0;
    }
    @catch(...)
    {
        DebugLog(@"Convert %@ to Pinyin failed!", self);        py = nil;
    }
    @finally
    {
        if(uniStr != NULL)
        {
            free(uniStr);
            uniStr = NULL;
        }
    }
    
    return py;
}

- (NSString*)sortedCharacter
{
    NSString* trimmingString = [self stringByTrimmingCharactersInSet:[NSCharacterSet whitespaceAndNewlineCharacterSet]];
    if ([trimmingString length] <= 0) {
        return @"{"; // 'z' + 1
    } else {
        unichar uni = [trimmingString characterAtIndex:0];
        if (uni >= 19968 && uni <= 40869) {
            uni = ChineseFirstPyTable[uni - 19968];
        } else if (uni >= 'a' && uni <= 'z') {
            uni = uni + 'A' - 'a';
        } else if (uni >= 'A' && uni <= 'Z') {
            
        } else {
            uni = '{';
        }
        return [NSString stringWithFormat:@"%c", uni];
    }
}

- (BOOL)isMatchPinYinString:(NSString*)matchString MatchRange:(NSRange*)matchRange
{
    NSString* namePY = [self pinyinString];
    NSRange range = [namePY rangeOfString:matchString options:NSCaseInsensitiveSearch];
    if (namePY != nil && range.length > 0) {
        if (matchRange){
            *matchRange = range;
        }
        return YES;
    }
    
    range = [namePY rangeOfString:[matchString substringToIndex:1] options:NSCaseInsensitiveSearch];
    // 第一个字符必须是首字母
    if (namePY != nil && range.length > 0) {
        NSString* nameFullPY = [self pinyinFullString];
        range = [nameFullPY rangeOfString:matchString options:NSCaseInsensitiveSearch];
        if (nameFullPY != nil && range.length > 0) {
            if (matchRange){
                *matchRange = range;
            }
            return YES;
        }
    }
    
    range = [self rangeOfString:matchString options:NSCaseInsensitiveSearch];
    if (range.length > 0) {
        if (matchRange){
            *matchRange = range;
        }
        return YES;
    }
    
    return NO;
}

